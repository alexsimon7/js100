let weather = 'cloudy';


switch (weather) {
  case 'sunny':
    console.log("It's a beautiful day!");
    break;
  case 'rainy':
    console.log('Grab your umbrella.');
    break;
  case 'windy':
    console.log("Brrr, it's chilly.");
    break;
  case 'hot':
    console.log("Sigh, it's a scorcher.");
    break;
  default:
    console.log("Let's stay inside.");
}

